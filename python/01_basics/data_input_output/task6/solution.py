# Считывание 3 чисел с клавиатуры
a, b, c = int(input()), int(input()), int(input())

# Вывод максимального числа из 3 введённых
print(max(a, b, c))

# Среднее число - это разность суммы начальных чисел и найденных двух значений
print(a + b + c - min(a, b, c) - max(a, b, c)) # Вывод среднего из 3 чисел

# Вывод минимального числа из 3 введённых
print(min(a, b, c))

